package org.example.homework3;

public class SimpleCalculator {
    public static void main(String[] args) {
        int a = 100;
        int b = 3;

        System.out.println("Sum variables: " + (a + b));
        System.out.println("Subtraction variables: " + (a - b));
        System.out.println("Multiplication variables: " + (a * b));
        System.out.println("Division variables: " + ((double)a / b));
    }
}
